//! Wire types for the InfiniteCode-compatible HTTP coordination layer.
//!
//! The HTTP layer is transport-neutral and intentionally mirrors the shape of
//! the InfiniteCode / Codebuff coordination API so that the same client SDKs work
//! against either backend. The agent stays client-side (InfiniteCode's runtime
//! is the same Rust code; only the coordination endpoints differ).
//!
//! These types are the source of truth for the JSON shapes exchanged with
//! `/api/v1/infinitecode/session`, `/api/v1/web-search`, `/api/v1/docs-search`,
//! `/api/v1/gravity-index`, `/api/v1/token-count`, `/api/v1/ads/*`, and the
//! auth / healthz shim. They are deliberately a small, self-contained subset;
//! see `crate::http` for the handler wiring.

use chrono::{DateTime, Utc};
use serde::{Deserialize, Serialize};
use smol_str::SmolStr;

/// Status of a coordination session, mirrored from the InfiniteCode-side enum
/// (`none | active | ended | superseded | country_blocked | banned |
/// rate_limited | takeover_prompt`).
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum CoordinationSessionStatus {
    /// Initial get is in-flight. Should resolve quickly; clients should poll.
    None,
    /// Session is admitted and active; the assigned model is live.
    Active,
    /// Session has been released by the client (DELETE /api/v1/infinitecode/session) or
    /// its grace period has elapsed. The model may still be finishing work on
    /// the agent side; this is informational only and not an error.
    Ended,
    /// Another client POSTed a new session for the same acting user id. The
    /// previous session is replaced; subsequent GETs on the old instance id
    /// should transition to "Superseded" UI state.
    Superseded,
    /// The requesting IP / country is outside the supported free-tier region.
    /// Terminal for the session — clients show the geofence screen.
    CountryBlocked,
    /// The acting user id is banned. Terminal.
    Banned,
    /// The acting user id hit a shared quota (e.g. premium model daily cap).
    /// Terminal for this tab — user can retry on a different model.
    RateLimited,
    /// Another local InfiniteCode CLI / Desktop instance already holds this
    /// acting user id and refused to be supplanted. Terminal.
    TakeoverPrompt,
}

/// Concurrency bucket the model occupies, mirrored from
/// `occupyInfiniteCodeDesktopSlot`. Controls whether the user can run another
/// premium-bucket tab concurrently.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum CoordinationSessionBucket {
    /// Premium bucket (DeepSeek V4 Pro, MiMo 2.5 Pro, Kimi K2.7 Code, HY3,
    /// KAT Coder Pro V2, MiniMax M3, GLM 5.2 — one per user at a time).
    Premium,
    /// Unlimited bucket (DeepSeek V4 Flash, MiMo 2.5 — no concurrency cap).
    Unlimited,
    /// Limited-access tier (every model into the single slot; one tab only).
    Limited,
}

/// Body of `POST /api/v1/infinitecode/session`.
///
/// The acting user id is required and is the rotation key: a new POST for the
/// same acting user id supersedes any prior active session for that user.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub struct CoordinationSessionRequest {
    /// Stable per-instance id generated by the client (typically a UUID). This
    /// is what the client polls in `GET /api/v1/infinitecode/session/:instance_id`
    /// and what headers carry (`x-infinitecode-instance-id`).
    pub instance_id: SmolStr,
    /// Stable per-user id from the auth login flow. Two sessions sharing this
    /// key will compete — only the most recent survives.
    pub acting_user_id: SmolStr,
    /// Model slug the client wants to use (e.g. `deepseek/deepseek-v4-pro`).
    pub model: SmolStr,
    /// Region / country ISO code; used by the country_blocked gate.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub iso_country_code: Option<SmolStr>,
    /// Stable device fingerprint hash for session-management.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub device_fingerprint: Option<SmolStr>,
    /// App version, surfaced in audit logs.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub app_version: Option<SmolStr>,
}

/// Body of `GET /api/v1/infinitecode/session` and the response of `POST`.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub struct CoordinationSessionResponse {
    pub instance_id: SmolStr,
    pub status: CoordinationSessionStatus,
    pub model: SmolStr,
    pub bucket: CoordinationSessionBucket,
    /// Remaining wall-clock time until the session expires, milliseconds.
    /// `None` when the session is not active.
    pub time_remaining_ms: Option<i64>,
    /// Server-assigned session start time, ISO-8601 UTC.
    pub started_at: DateTime<Utc>,
    /// Server-assigned session expiry when the session is finite, ISO-8601
    /// UTC. `None` represents "no fixed end" (chat-only ad-hoc poster).
    pub expires_at: Option<DateTime<Utc>>,
    /// Stable opaque reason string for terminal statuses (e.g.
    /// `'shared_premium_quota'`, `'cn_geofence'`).
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub reason: Option<SmolStr>,
    /// Seconds until the user's shared daily quota resets (Pacific time).
    /// `None` if not applicable.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub next_quota_reset_secs: Option<i64>,
}

/// `POST /api/v1/web-search` proxy request.
///
/// The server forwards this to a Serper-shaped upstream and normalises the
/// response into a single `result` string for the agent.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub struct WebSearchRequest {
    pub query: SmolStr,
    #[serde(default = "default_search_depth")]
    pub depth: WebSearchDepth,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub repo_url: Option<SmolStr>,
}

fn default_search_depth() -> WebSearchDepth {
    WebSearchDepth::Standard
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum WebSearchDepth {
    Standard,
    Deep,
}

#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub struct WebSearchResponse {
    pub result: SmolStr,
    #[serde(default)]
    pub credits_used: u64,
}

