/// JSON-RPC 2.0 error envelope mirrored from the InfiniteCode-side response
/// shape so clients can reuse the same parser.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub struct CoordinationErrorBody {
    pub code: i32,
    pub message: SmolStr,
    /// Optional machine-readable status discriminator (e.g. `superseded`).
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub status: Option<SmolStr>,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub reason: Option<SmolStr>,
}

impl CoordinationErrorBody {
    /// A short, stable numeric error code. Mirrored from the InfiniteCode-side
    /// `AcpErrorCode`-shaped enum.
    pub const SESSION_SUPERSEDED: i32 = 409_001;
    pub const SESSION_RATE_LIMITED: i32 = 429_001;
    pub const SESSION_COUNTRY_BLOCKED: i32 = 451_001;
    pub const AUTH_REQUIRED: i32 = 401_000;
    pub const AUTH_INVALID: i32 = 401_001;
    pub const BAD_REQUEST: i32 = 400_000;
    pub const INTERNAL: i32 = 500_000;
    pub const NOT_IMPLEMENTED: i32 = 501_001;
}
