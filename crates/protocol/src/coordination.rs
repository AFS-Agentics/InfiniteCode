/// Auth shim.
///
/// `POST /api/v1/auth/login` takes a configured password (operator supplies
/// via `[infinitecode_bridge].password` in `config.toml` or
/// `INFINITECODE_BRIDGE_PASSWORD` env override) and mints a
/// long-lived bearer token. Subsequent endpoints validate against the same
/// shared secret.
///
/// The shim is intentionally minimal: the agent's own auth lives in
/// NextAuth; this layer is for the coordination API only.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub struct AuthLoginRequest {
    pub password: SmolStr,
}

#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub struct AuthLoginResponse {
    pub token: SmolStr,
    /// Unix epoch seconds until token expiry (24h by default).
    pub expires_at: i64,
}

